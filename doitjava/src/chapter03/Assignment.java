package chapter03;

public class Assignment {

	public static void main(String[] args) {
		int result = 0;   //왼쪽(0)을 오른쪽(10)에 더해라 해서 나온게 예제1
		result += 10;
		System.out.println("result = " + result);
		result -= 5;
		System.out.println("result = " + result);
		result *= 3;
		System.out.println("result = " + result);
		result /= 5;
		System.out.println("result = " + result);
		result %= 3;
		System.out.println("result = " + result);
		
		
	}

}
